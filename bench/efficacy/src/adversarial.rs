/// Drive the real Suricata binary over the adversarial IPS corpus.
pub async fn run_ips() -> FunctionReport {
    let version = match suricata_available().await {
        Some(v) => v,
        None => {
            return FunctionReport::untested(
                "ips_adversarial",
                "sng-ips",
                Kind::Detection,
                "suricata binary not found on PATH; install Suricata to measure IPS detection efficacy",
            );
        }
    };

    let fixtures = ips_adversarial_dir();
    let rules = fixtures.join("adversarial.rules");
    if !rules.exists() {
        return FunctionReport::untested(
            "ips_adversarial",
            "sng-ips",
            Kind::Detection,
            "adversarial IPS fixtures missing (expected bench/efficacy/fixtures/ips/adversarial)",
        );
    }

    let work = std::env::temp_dir().join(format!("sng-efficacy-ips-adv-{}", std::process::id()));
    if let Err(e) = tokio::fs::create_dir_all(&work).await {
        return FunctionReport::untested(
            "ips_adversarial",
            "sng-ips",
            Kind::Detection,
            &format!("could not create IPS work dir {}: {e}", work.display()),
        );
    }
    let _work_guard = WorkDirGuard(work.clone());
    let eve = work.join("eve.json");
    let stats_sock = work.join("suricata-command.socket");
    let yaml_path = work.join("suricata.yaml");

    let yaml = match render_config(&rules, &eve, &stats_sock) {
        Ok(y) => y,
        Err(e) => {
            return FunctionReport::untested("ips_adversarial", "sng-ips", Kind::Detection, &e)
        }
    };
    if let Err(e) = tokio::fs::write(&yaml_path, &yaml).await {
        return FunctionReport::untested(
            "ips_adversarial",
            "sng-ips",
            Kind::Detection,
            &format!("could not write rendered suricata.yaml: {e}"),
        );
    }

    let mut cases = Vec::new();
    for c in ips_corpus() {
        let pcap = fixtures.join(c.file);
        let n = match alerts_for_pcap(&yaml_path, &pcap, &work, &eve).await {
            Ok(n) => n,
            Err(e) => {
                return FunctionReport::untested(
                    "ips_adversarial",
                    "sng-ips",
                    Kind::Detection,
                    &format!("suricata execution failed on {}: {e}", c.file),
                );
            }
        };
        let alerted = n > 0;
        let correct = if c.bad { alerted } else { !alerted };
        cases.push(Case {
            description: c.desc.into(),
            bad: c.bad,
            expected: if c.bad { "detect" } else { "no-alert" }.into(),
            actual: format!("{n} alert(s)"),
            correct,
        });
    }

    FunctionReport::from_cases(
        "ips_adversarial",
        "sng-ips",
        Kind::Detection,
        Targets::default(),
        cases,
        Some(format!(
            "Real {version} driven by SNG's ConfigGenerator-rendered suricata.yaml over \
             an evasion corpus: fragmented HTTP and session-spliced TCP (caught after \
             defrag + stream reassembly), double-encoded URIs and DoH tunneling (caught \
             by evasion-aware signatures). EVE alerts normalised through \
             sng_ips::EveAlert::to_ips_event; offline PCAP replay in IDS mode."
        )),
    )
    .with_features(vec![
        Feature {
            name: "Reassembly-resilient detection".into(),
            how: "Suricata defrag + TCP stream reassembly rebuild the payload before the \
                  content match, so a pattern split across IP fragments or one-byte \
                  segments is still caught."
                .into(),
            coverage: "fragmented HTTP, session-spliced SQLi / traversal".into(),
        },
        Feature {
            name: "Evasion-aware signatures".into(),
            how: "rules match the double-percent-encoded bytes and the DoH wire markers \
                  directly, catching evasions that survive a single decode pass."
                .into(),
            coverage: "double-encoded ../ and <script>, DoH dns-message / dns-query".into(),
        },
    ])
}
