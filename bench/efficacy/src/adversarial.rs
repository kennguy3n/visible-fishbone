//! Adversarial efficacy: drive the *real* `sng_swg::YaraEngine` and
//! Suricata over corpora built specifically to **evade** signature
//! detection, and report them as their own rows in the efficacy matrix
//! (`malware_adversarial`, `ips_adversarial`).
//!
//! Where [`crate::malware`] and [`crate::ips`] measure detection of the
//! canonical, cleanly-framed attack shapes, this module measures
//! detection of the obfuscation / evasion shapes a motivated attacker
//! reaches for:
//!
//! * **malware_adversarial** — UPX-packed executables, archive-smuggled
//!   / double-extension droppers, HTML smuggling (base64 -> Blob ->
//!   download), polyglot PDF + auto-exec JavaScript, obfuscated VBA macro
//!   source, encoded / download-cradle PowerShell, and eval-equivalent
//!   JavaScript (`Function`/`setTimeout`/indirect `eval` over
//!   `atob`/`unescape`). Driven through the real
//!   `YaraEngine::with_builtin_rules` scan path.
//!
//! * **ips_adversarial** — fragmented HTTP, session-spliced TCP,
//!   double-encoded URIs and DoH tunneling, replayed through the real
//!   Suricata binary (defrag + stream reassembly + normalisation) using
//!   SNG's `ConfigGenerator`-rendered `suricata.yaml`.
//!
//! Every sample is a benign synthetic stand-in (EICAR markers, minimal
//! valid headers, inert payloads); no live malware is used. The fixtures
//! are committed and regenerated by the `gen_samples.py` /
//! `gen_adversarial_pcaps.py` scripts next to them.

use std::path::PathBuf;

use serde::Deserialize;
use sng_swg::{YaraEngine, YaraSeverity};

use crate::ips::{alerts_for_pcap, render_config, suricata_available, WorkDirGuard};
use crate::report::{Case, Feature, FunctionReport, Kind, Targets};

// ===== malware_adversarial =============================================

fn malware_fixtures_dir() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("fixtures/malware/adversarial")
}

/// One entry of the committed `manifest.json`: a fixture file, whether
/// it is known-bad, the malware family it represents, and a human label.
#[derive(Debug, Deserialize)]
struct ManifestEntry {
    file: String,
    bad: bool,
    #[allow(dead_code)]
    family: String,
    desc: String,
}

/// Drive the real `YaraEngine` over the adversarial malware corpus.
pub async fn run_malware() -> FunctionReport {
    let engine = match YaraEngine::with_builtin_rules() {
        Ok(e) => e,
        Err(e) => {
            return FunctionReport::untested(
                "malware_adversarial",
                "sng-swg",
                Kind::Detection,
                &format!("YARA built-in rules failed to compile: {e}"),
            );
        }
    };

    let dir = malware_fixtures_dir();
    let manifest_path = dir.join("manifest.json");
    let manifest_text = match std::fs::read_to_string(&manifest_path) {
        Ok(t) => t,
        Err(e) => {
            return FunctionReport::untested(
                "malware_adversarial",
                "sng-swg",
                Kind::Detection,
                &format!(
                    "adversarial malware manifest missing ({}): {e}",
                    manifest_path.display()
                ),
            );
        }
    };
    let manifest: Vec<ManifestEntry> = match serde_json::from_str(&manifest_text) {
        Ok(m) => m,
        Err(e) => {
            return FunctionReport::untested(
                "malware_adversarial",
                "sng-swg",
                Kind::Detection,
                &format!("could not parse adversarial malware manifest: {e}"),
            );
        }
    };

    let mut cases = Vec::new();
    for entry in &manifest {
        let path = dir.join(&entry.file);
        let content = match std::fs::read(&path) {
            Ok(c) => c,
            Err(e) => {
                return FunctionReport::untested(
                    "malware_adversarial",
                    "sng-swg",
                    Kind::Detection,
                    &format!("could not read adversarial sample {}: {e}", path.display()),
                );
            }
        };
        let matches = engine.scan(&content);
        let detected = !matches.is_empty();
        let actual = if detected {
            let worst = matches
                .iter()
                .map(|m| m.severity)
                .max()
                .unwrap_or(YaraSeverity::Suspicious);
            format!("detect ({})", worst.as_str())
        } else {
            "pass".to_string()
        };
        let correct = if entry.bad { detected } else { !detected };
        cases.push(Case {
            description: entry.desc.clone(),
            bad: entry.bad,
            expected: if entry.bad { "detect" } else { "pass" }.into(),
            actual,
            correct,
        });
    }

    FunctionReport::from_cases(
        "malware_adversarial",
        "sng-swg",
        Kind::Detection,
        Targets::default(),
        cases,
        Some(
            "Real YaraEngine::scan over an evasion-focused corpus (UPX packing, \
             archive smuggling / double extensions, HTML smuggling, polyglot PDF+JS, \
             obfuscated VBA, encoded PowerShell, eval-equivalent JS). Benign \
             look-alikes (base64 data-URI pages, admin scripts, macro-free docs, \
             clean PDFs/ZIPs) pass clean to bound the false-positive rate."
                .into(),
        ),
    )
    .with_features(vec![
        Feature {
            name: "Evasion-shape signature coverage".into(),
            how: "yara-x built-in rules extended with packer, archive-smuggling, \
                  HTML-smuggling, PDF-JS, VBA-obfuscation, encoded-PowerShell and \
                  eval-equivalent-JS signatures; scan() runs them over file bytes."
                .into(),
            coverage: "UPX, double-extension ZIP, base64 Blob HTML, polyglot PDF+JS, \
                       Chr()-built VBA, -EncodedCommand PowerShell, Function(atob())"
                .into(),
        },
        Feature {
            name: "Archive name inspection without unpacking".into(),
            how: "matches the plaintext ZIP local-file-header filename so a deflated \
                  payload's dangerous / double extension is caught without decompression."
                .into(),
            coverage: "invoice.pdf.exe, *.scr/*.hta/*.vbe inside ZIP".into(),
        },
    ])
}

